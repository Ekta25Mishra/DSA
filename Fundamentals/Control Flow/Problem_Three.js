const num=Number(readlineSync.question("Enter the number:"));
console.log("Total number of Notes=>");
let a=Math.floor(num/500);
console.log(`500=${a}`);
let b=num-(500*a);
let c=Math.floor(b/100);
console.log(`100=${c}`);
let d=b-(100*c);
let e=Math.floor(d/50);
console.log(`50=${e}`);
let f=d-(50*e);
let g=Math.floor(f/20);
console.log(`20=${g}`);
let h=f-(g*20);
let i=Math.floor(h/10);
console.log(`10=${i}`);
let j=h-(i*10);
